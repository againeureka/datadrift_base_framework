    <script>
        document.querySelector('form').addEventListener('submit', function(event) {
            event.preventDefault();  // 기본 폼 제출 동작을 방지
            var form = this;

            var formData = new FormData(form);

            fetch(form.action, {
                method: 'POST',
                body: formData
            }).then(response => {
                if (response.ok) {
                    // 회원가입 성공 후 상위 페이지(부모 페이지)를 새로고침
                    window.top.location.reload();  // 상위 페이지를 새로고침
                } else {
                    // 실패한 경우 (회원가입 실패 처리)
                    console.log('Registration failed');
                }
            }).catch(error => {
                console.error('Error:', error);
            });
        });
    </script>

